package com.example.bank.demo.rest;

import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;

@ControllerAdvice
public class TransactionRestExceptionHandler {

    @ExceptionHandler
        //specifies the type of the exception
    ResponseEntity<TransactionErrorResponse> handleException(TransactionNotFoundException exc) {
        //create response

        TransactionErrorResponse error = new TransactionErrorResponse();
        error.setStatus(HttpStatus.NOT_FOUND.value());
        error.setMessage(exc.getMessage());
        error.setTimeStamp(System.currentTimeMillis());

        return new ResponseEntity<>(error, HttpStatus.NOT_FOUND);
    }

    @ExceptionHandler
    public ResponseEntity<TransactionErrorResponse> handleException(Exception exc) { //handles generic exceptions

        // create a UserErrorResponse
        TransactionErrorResponse error = new TransactionErrorResponse();

        error.setStatus(HttpStatus.BAD_REQUEST.value());
        error.setMessage(exc.getMessage());
        error.setTimeStamp(System.currentTimeMillis());

        // return ResponseEntity
        return new ResponseEntity<>(error, HttpStatus.BAD_REQUEST);
    }
}
